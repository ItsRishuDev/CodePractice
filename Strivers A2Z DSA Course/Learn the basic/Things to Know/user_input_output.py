"""
Complete the function printNumber which takes an integer input from the user and prints it on the screen.



Use:-

for Python : print()

Examples:
Input(user gives value): 7

Output: 7

Input(user gives value): -5

Output: -5
"""

class Solution:
    def printNumber(self):
        num = input()
        print(num)

solution_obj = Solution()
